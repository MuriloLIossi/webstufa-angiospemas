<h1>Importância e Usos das Angiospermas</h1>

  <section style="margin-top: 30px">
  <p>As <strong>angiospermas</strong>, também conhecidas como <em>plantas com flores</em>, são essenciais para a vida no planeta. Elas representam o grupo mais diverso do reino vegetal e oferecem inúmeros recursos para a humanidade, além de manterem o equilíbrio dos ecossistemas.</p>
  </section>

  <section>
    <h2>Alimentação</h2>
    <p>Grande parte dos alimentos que consumimos diariamente — como <strong>grãos, frutas, legumes, nozes, óleos</strong> e <strong>especiarias</strong> — é proveniente de angiospermas. Elas são a base da dieta humana e da nutrição de muitos animais.</p>
  </section>

  <section>
    <h2>Madeira</h2>
    <p>Árvores angiospermas, especialmente de regiões tropicais, fornecem <strong>madeira de qualidade</strong> utilizada na construção civil, móveis, artesanato e diversos produtos industriais.</p>
  </section>

  <section>
    <h2>Medicamentos</h2>
    <p>Muitas angiospermas possuem propriedades <strong>fitoterápicas e medicinais</strong>. Elas são empregadas no tratamento de doenças tanto na medicina tradicional quanto na moderna.</p>
  </section>

  <section>
    <h2>Especiarias e Bebidas</h2>
    <p>Especiarias como <strong>pimenta, cravo, canela</strong> e <strong>cardamomo</strong> vêm das angiospermas. Bebidas como <strong>chá</strong> e <strong>café</strong> também são produzidas a partir dessas plantas.</p>
  </section>

  <section>
    <h2>Polinização e Biodiversidade</h2>
    <p>As flores das angiospermas são cruciais para a <strong>polinização</strong>, atraindo abelhas, borboletas e pássaros. Esse processo garante a <strong>reprodução vegetal</strong> e mantém a <strong>biodiversidade dos ecossistemas</strong>.</p>
  </section>

  <section>
    <h2>Sustentabilidade e Ecossistemas</h2>
    <p>As florestas de angiospermas desempenham papel essencial na <strong>regulação climática</strong>, <strong>conservação da água</strong> e <strong>proteção do solo</strong>, além de contribuírem para o <strong>sequestro de carbono</strong> e combate às mudanças climáticas.</p>
  </section>
@endsection