<h1><strong>Características Gerais das plantas</strong></h1>
<h2>Organização Celular</h2>
<p>Todas as plantas possuem <strong>células eucariontes</strong>, ou seja, com núcleo organizado. Elas também apresentam organelas exclusivas, como os <strong>cloroplastos</strong>, responsáveis pela fotossíntese.</p>

<hr>

<h2>Fotossíntese</h2>
<p>A maioria das plantas realiza fotossíntese — processo em que obtêm alimento utilizando a <strong>luz do sol</strong>, <strong>água</strong> e <strong>nutrientes do solo</strong>.</p>

<hr>

<h2>Nutrição Autotrófica</h2>
<p>A esmagadora maioria das plantas é <strong>autotrófica</strong>, ou seja, fabrica o próprio alimento. A <strong>glicose</strong>, essencial para a planta, é produzida durante a fotossíntese.</p>

<hr>

<h2>Reprodução</h2>
<p>Processo de formação de novos indivíduos, garantindo a continuidade da espécie:</p>
<ul>
  <li><strong>Sexuada:</strong> Envolve gametas masculino e feminino, resultando em variabilidade genética.</li>
  <li><strong>Assexuada:</strong> Uma planta mãe gera descendentes idênticos (clones), por meio de:
    <ul>
      <li>Brotamento</li>
      <li>Rizomas</li>
      <li>Estolhos</li>
    </ul>
  </li>
</ul>

<hr>

<h2>Adaptações ao Ambiente</h2>
<p>Ao longo da evolução, as plantas desenvolveram <strong>características adaptativas</strong> para sobreviver em diferentes condições ambientais.</p>
<p>Exemplo: <em>Xerófitas</em> como os <strong>cactos</strong>, adaptadas a ambientes quentes e secos.</p>

<hr>

<h2>Transporte de Substâncias</h2>
<p>A partir das <strong>pteridófitas</strong>, surgem os <strong>vasos condutores</strong>:</p>
<ul>
  <li><strong>Xilema:</strong> transporta seiva bruta (água e sais minerais) da raiz para as folhas.</li>
  <li><strong>Floema:</strong> transporta seiva elaborada (glicose) das folhas para o restante da planta.</li>
</ul>
<p>Esses vasos usam o princípio da <strong>capilaridade</strong>. Já nas <strong>briófitas</strong>, o transporte é célula a célula, limitando o tamanho da planta.</p>

<hr>

<h2>Respiração Celular</h2>
<p>É o processo em que a planta transforma <strong>glicose</strong> em <strong>energia</strong>. Isso ocorre em todas as células, <strong>durante o dia e a noite</strong>.</p>
<p>A planta usa <strong>oxigênio</strong> e libera <strong>dióxido de carbono</strong> e <strong>água</strong> nesse processo.</p>

